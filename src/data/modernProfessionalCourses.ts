import type { Course, Lesson, PracticeActivity, PracticeActivityType, QuizQuestion } from "@/data/mockData";

type ModernLessonDraft = {
  id: string;
  title: string;
  module: string;
  estimatedMinutes?: number;
  learningObjective: string;
  description: string;
  explanation: string;
  analogy: string;
  example: string;
  codeExample: string;
  starterCode: string;
  solution: string;
  expectedOutput: string;
  hints: string[];
  summary: string;
  nextStep: string;
  tryItPrompt: string;
  commonMistake: string;
  reference: string[];
  concepts: string[];
  contrastExample?: {
    wrong: string;
    right: string;
    explanation: string;
  };
  quiz: {
    question: string;
    options: string[];
    correctIndex: number;
    success: string;
    error: string;
    hint: string;
  };
  practice: {
    type?: PracticeActivityType;
    title: string;
    prompt: string;
    code?: string;
    options?: string[];
    correctAnswer: string | string[];
    success: string;
    error: string;
    hint: string;
  };
  xpReward?: number;
};

function buildQuiz(draft: ModernLessonDraft["quiz"]): QuizQuestion[] {
  return [
    {
      question: draft.question,
      options: draft.options,
      correctIndex: draft.correctIndex,
      explanation: draft.success,
      successFeedback: draft.success,
      errorFeedback: draft.error,
      hint: draft.hint,
    },
  ];
}

function buildPractice(id: string, draft: ModernLessonDraft["practice"]): PracticeActivity[] {
  return [
    {
      id: `${id}-practice`,
      type: draft.type ?? "mini-challenge",
      title: draft.title,
      prompt: draft.prompt,
      code: draft.code,
      options: draft.options,
      correctAnswer: draft.correctAnswer,
      successFeedback: draft.success,
      errorFeedback: draft.error,
      hint: draft.hint,
    },
  ];
}

function makeLesson(draft: ModernLessonDraft): Lesson {
  return {
    id: draft.id,
    title: draft.title,
    module: draft.module,
    level: "Iniciante",
    estimatedMinutes: draft.estimatedMinutes ?? 7,
    description: draft.description,
    theory: `${draft.explanation}

Exemplo prático:
${draft.example}

Erro comum:
${draft.commonMistake}

Referência rápida:
${draft.reference.map((item) => `- ${item}`).join("\n")}`,
    starterCode: draft.starterCode,
    solution: draft.solution,
    expectedOutput: draft.expectedOutput,
    hints: draft.hints,
    xpReward: draft.xpReward ?? 15,
    learningObjective: draft.learningObjective,
    analogy: draft.analogy,
    example: draft.example,
    codeExample: draft.codeExample,
    summary: draft.summary,
    nextStep: draft.nextStep,
    tryItPrompt: draft.tryItPrompt,
    commonMistake: draft.commonMistake,
    contrastExample: draft.contrastExample,
    reference: draft.reference,
    concepts: draft.concepts,
    quiz: buildQuiz(draft.quiz),
    practiceActivities: buildPractice(draft.id, draft.practice),
  };
}

